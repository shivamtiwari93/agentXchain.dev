import { describe, it } from 'node:test';
import assert from 'node:assert/strict';
import { readFileSync, existsSync } from 'node:fs';
import { resolve } from 'node:path';

const WEBSITE_ROOT = resolve(import.meta.dirname, '../../website-v2');

describe('Docs System Evaluation — acceptance tests (DEC-DOCS-SYSTEM-001)', () => {
  const sidebars = readFileSync(resolve(WEBSITE_ROOT, 'sidebars.ts'), 'utf8');
  const config = readFileSync(resolve(WEBSITE_ROOT, 'docusaurus.config.ts'), 'utf8');

  it('AT-DSE-001: sidebars.ts uses autogenerated for release notes', () => {
    assert.ok(sidebars.includes("type: 'autogenerated'"), 'sidebar must use autogenerated type');
    assert.ok(sidebars.includes("dirName: 'releases'"), 'sidebar must target releases directory');
  });

  it('AT-DSE-002: sitemap is not disabled', () => {
    assert.ok(!config.includes('sitemap: false'), 'sitemap must not be disabled');
    assert.ok(config.includes('sitemap:'), 'sitemap config must be present');
  });

  it('AT-DSE-003: no manual sitemap.xml in static/', () => {
    assert.ok(
      !existsSync(resolve(WEBSITE_ROOT, 'static/sitemap.xml')),
      'manual sitemap.xml must not exist — Docusaurus generates it',
    );
  });

  it('AT-DSE-006: docusaurus.config.ts includes local search theme', () => {
    assert.ok(
      config.includes('@easyops-cn/docusaurus-search-local'),
      'config must include the local search plugin',
    );
  });

  it('AT-DSE-006b: search plugin is configured for docs only', () => {
    assert.ok(config.includes('indexBlog: false'), 'search must not index blog');
    assert.ok(config.includes('hashed: true'), 'search index must be hashed for cache busting');
  });
});
